    <h3>International Patient Services</h3>

Grande International Hospital has special arrangements for foreign citizens so that they do not have to worry about taking healthcare facilities in Nepal. A thoughtfully set up help desk takes care of international patients’ insurance and arranges for the appointment with physicians and surgeons. With all the paperwork and documentation taken care of, our foreign patients’ will feel at home and privileged.
Assisted Fertility Center

Grande International Hospital’s Assisted Fertility Center is a dedicated department that has been setup to help parents who have not been able to conceive the natural way. It has been predicted that almost 10% of married couples have problems getting pregnant. Our center has the latest technology and the best trained doctors to help couples complete their family. With services like Infertility workup, genetic evaluation, Intra Uterine Insemination, In Vitro Fertilization and Intracytoplasmic sperm injection, our doctors provide the best results in terms of successful pregnancies in the region.
